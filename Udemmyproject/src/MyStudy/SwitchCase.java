package MyStudy;

public class SwitchCase {
    public static void main(String[] args) {
        int number=90;

        switch(number){
            case 1:
                System.out.println("one");
            case 2:
                System.out.println("two");

            case 3:
                System.out.println("three");

            case 4:
                System.out.println("four");
                break;

            default:
                System.out.println("invalid:");
                break;



        }
    }
}
